terminal命令总结：
    https://blog.csdn.net/guyue35/article/details/53419373
    Ctrl+shift+o    竖直新建屏幕
    Ctrl+shift+e    水平新建屏幕
    Alt+箭头       同一个窗口上下左右切换
    Ctrl+PgUp/PgDn  不同窗口切换


ctrl + l    清屏
Ctrl+a	光标移动到开始位置
Ctrl+e	光标移动到最末尾
Ctrl+k	删除此处至末尾的所有内容
Ctrl+u	删除此处至开始的所有内容
Ctrl+d	删除当前字符


简繁体切换： Ctrl+shift+f


